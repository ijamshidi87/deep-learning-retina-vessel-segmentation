import torch
import torch.nn as nn


# Basic Conv Block
class ConvBlock(nn.Module):
    """Two-layer conv block with BatchNorm and optional Dropout."""
    def __init__(self, in_channels, out_channels, dropout=0.1):
        super(ConvBlock, self).__init__()
        self.conv = nn.Sequential(
            nn.Conv2d(in_channels, out_channels, kernel_size=3, padding=1),
            nn.BatchNorm2d(out_channels),
            nn.ReLU(inplace=True),
            nn.Dropout2d(dropout),
            nn.Conv2d(out_channels, out_channels, kernel_size=3, padding=1),
            nn.BatchNorm2d(out_channels),
            nn.ReLU(inplace=True)
        )

    def forward(self, x):
        return self.conv(x)


# U-Net 2-Block
class UNet2Block(nn.Module):
    """Compact U-Net with 2 encoder-decoder blocks."""
    def __init__(self, in_channels=3, out_channels=1, dropout=0.1):
        super(UNet2Block, self).__init__()

        # Encoder
        self.enc1 = ConvBlock(in_channels, 64, dropout)
        self.pool1 = nn.MaxPool2d(2)
        self.enc2 = ConvBlock(64, 128, dropout)
        self.pool2 = nn.MaxPool2d(2)

        # Bottleneck
        self.bottleneck = ConvBlock(128, 256, dropout)

        # Decoder
        self.up2 = nn.ConvTranspose2d(256, 128, kernel_size=2, stride=2)
        self.dec2 = ConvBlock(256, 128, dropout)

        self.up1 = nn.ConvTranspose2d(128, 64, kernel_size=2, stride=2)
        self.dec1 = ConvBlock(128, 64, dropout)

        # Output
        self.out_conv = nn.Conv2d(64, out_channels, kernel_size=1)

    def forward(self, x):
        e1 = self.enc1(x)
        p1 = self.pool1(e1)

        e2 = self.enc2(p1)
        p2 = self.pool2(e2)

        b = self.bottleneck(p2)

        up2 = self.up2(b)
        up2 = torch.cat([up2, e2], dim=1)
        d2 = self.dec2(up2)

        up1 = self.up1(d2)
        up1 = torch.cat([up1, e1], dim=1)
        d1 = self.dec1(up1)

        out = torch.sigmoid(self.out_conv(d1))
        return out


# U-Net 3-Block
class UNet3Block(nn.Module):
    """Deeper U-Net with 3 encoder-decoder blocks."""
    def __init__(self, in_channels=3, out_channels=1, dropout=0.1):
        super(UNet3Block, self).__init__()

